# Solicita dois números ao usuário, soma e exibe o resultado
n1 = int(input('Digite um número: '))  # Lê o primeiro número
n2 = int(input('Digite outro: '))      # Lê o segundo número
s = n1 + n2                           # Calcula a soma
print('A soma entre {} e {} vale {}' .format(n1, n2, s))  # Exibe o resultado